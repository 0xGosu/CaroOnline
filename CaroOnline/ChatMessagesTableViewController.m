- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *CellIdentifier = @"Message";
    
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CellIdentifier];
    if (cell == nil) {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:CellIdentifier] autorelease];
		cell.backgroundColor=[UIColor clearColor];		
		cell.selectionStyle = UITableViewCellSelectionStyleNone;
		cell.textLabel.lineBreakMode = UILineBreakModeWordWrap;
		cell.textLabel.numberOfLines = 3;
		cell.textLabel.font = [UIFont fontWithName:@"Helvetica" size:13];
		cell.textLabel.textColor= [UIColor blackColor];
		
	}
    
    // Configure the cell...
	if(!reverse ){
		cell.textLabel.text = [messages objectAtIndex:indexPath.row];
		//cell.detailTextLabel.text = [messages objectAtIndex:indexPath.row];
	}
	else {
		cell.textLabel.text = [messages objectAtIndex:[messages count]-1-indexPath.row];
		//cell.detailTextLabel.text = [messages objectAtIndex:[messages count]-1-indexPath.row];
	}
	
    return cell;
}

